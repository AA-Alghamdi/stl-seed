# BestOfNSampler
# ---------------------------------------------------------------------------


class BestOfNSampler:
    """Standard Best-of-N: binary STL acceptance, post-hoc filtering.

    Generates ``n`` candidate trajectories, returns the *first* one with
    ``rho > 0`` (binary STL satisfaction). If none satisfies, returns the
    first sample with diagnostic ``found_satisfying = False``.

    This is the "filter then output" baseline matching the SERA pipeline
    convention. It throws away the magnitude of ``rho``.

    Parameters
    ----------
    n:
        Sample budget.
    rho_threshold:
        Binary acceptance threshold. Default 0.0 (Donzé-Maler convention:
        ``rho > 0`` iff trajectory satisfies the spec).

    All other parameters: see :class:`StandardSampler`.
    """

    def __init__(
        self,
        llm: LLMProposal,
        simulator: Any,
        spec: STLSpec | Node,
        action_vocabulary: jt.Float[jt.Array, "K m"],
        sim_params: Any,
        *,
        horizon: int,
        n: int = 8,
        rho_threshold: float = 0.0,
        aux: dict[str, Any] | None = None,
        sampling_temperature: float = 1.0,
    ) -> None:
        if n < 1:
            raise ValueError(f"n must be >= 1, got {n}")
        self.llm = llm
        self.simulator = simulator
        self.spec = spec
        self.vocabulary = jnp.asarray(action_vocabulary, dtype=jnp.float32)
        if self.vocabulary.ndim != 2:
            raise ValueError(
                f"action_vocabulary must be 2-d (K, m), got shape {self.vocabulary.shape}"
            )
        self.K, self.m = int(self.vocabulary.shape[0]), int(self.vocabulary.shape[1])
        self.sim_params = sim_params
        self.horizon = int(horizon)
        if self.horizon < 1:
            raise ValueError(f"horizon must be >= 1, got {self.horizon}")
        self.aux = dict(aux) if aux is not None else None
        self.n = int(n)
        self.rho_threshold = float(rho_threshold)
        self.sampling_temperature = float(sampling_temperature)
        self._compiled_spec = compile_spec(spec)

    def sample(
        self,
        initial_state: jt.Float[jt.Array, " n"],
        key: jt.PRNGKeyArray,
    ) -> SamplerResult:
        all_traj: list[Trajectory] = []
        all_rho: list[float] = []
        chosen: int = 0
        found_satisfying = False
        for i in range(self.n):
            sub_key = jax.random.fold_in(key, i)
            traj = _rollout(
                self.llm,
                self.simulator,
                self.sim_params,
                self.aux,
                self.vocabulary,
                self.horizon,
                initial_state,
                sub_key,
                self.sampling_temperature,
            )
            rho = float(self._compiled_spec(traj.states, traj.times))
            all_traj.append(traj)
            all_rho.append(rho)
            if not found_satisfying and rho > self.rho_threshold:
                chosen = i
                found_satisfying = True
                # Note: we keep generating to fill diagnostics for
                # apples-to-apples compute comparison with the gradient-
                # guided sampler. The chosen trajectory is the *first*
                # acceptable one (canonical BoN early-stop semantics on
                # the *return*, not on the *compute*). If callers want
                # early-stop compute too, set n=1 and call repeatedly.
        diag: SamplerDiagnostics = {
            "sampler": "best_of_n",
            "n_samples": self.n,
            "all_rho": all_rho,
            "chosen_index": chosen,
            "chosen_rho": all_rho[chosen],
            "max_rho": max(all_rho),
            "found_satisfying": found_satisfying,
            "rho_threshold": self.rho_threshold,
            "final_rho": all_rho[chosen],
            "sampling_temperature": self.sampling_temperature,
        }
        return all_traj[chosen], diag


