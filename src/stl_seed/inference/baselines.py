class ContinuousBoNSampler:
    """BoN with continuous ``rho`` scoring: argmax-rho selection.

    Generates ``n`` candidate trajectories and returns the one with the
    highest ``rho``. Unlike :class:`BestOfNSampler`, this uses the full
    real-valued ``rho`` rather than a binary acceptance threshold, so it
    benefits from the same continuous semantics that
    :class:`STLGradientGuidedSampler` exploits. but only post-hoc.

    The hypothesis underlying gradient-guided sampling is that
    *during-sampling* use of the gradient strictly dominates *post-hoc*
    use of the same continuous signal at matched sample budget. This
    sampler is the strict baseline that hypothesis must beat.

    Parameters: same as :class:`BestOfNSampler` (``n`` is the budget;
    no ``rho_threshold``. selection is unconditional argmax).
    """

    def __init__(
        self,
        llm: LLMProposal,
        simulator: Any,
        spec: STLSpec | Node,
        action_vocabulary: jt.Float[jt.Array, "K m"],
        sim_params: Any,
        *,
        horizon: int,
        n: int = 8,
        aux: dict[str, Any] | None = None,
        sampling_temperature: float = 1.0,
    ) -> None:
        if n < 1:
            raise ValueError(f"n must be >= 1, got {n}")
        self.llm = llm
        self.simulator = simulator
        self.spec = spec
        self.vocabulary = jnp.asarray(action_vocabulary, dtype=jnp.float32)
        if self.vocabulary.ndim != 2:
            raise ValueError(
                f"action_vocabulary must be 2-d (K, m), got shape {self.vocabulary.shape}"
            )
        self.K, self.m = int(self.vocabulary.shape[0]), int(self.vocabulary.shape[1])
        self.sim_params = sim_params
        self.horizon = int(horizon)
        if self.horizon < 1:
            raise ValueError(f"horizon must be >= 1, got {self.horizon}")
        self.aux = dict(aux) if aux is not None else None
        self.n = int(n)
        self.sampling_temperature = float(sampling_temperature)
        self._compiled_spec = compile_spec(spec)

    def sample(
        self,
        initial_state: jt.Float[jt.Array, " n"],
        key: jt.PRNGKeyArray,
    ) -> SamplerResult:
        all_traj: list[Trajectory] = []
        all_rho: list[float] = []
        for i in range(self.n):
            sub_key = jax.random.fold_in(key, i)
            traj = _rollout(
                self.llm,
                self.simulator,
                self.sim_params,
                self.aux,
                self.vocabulary,
                self.horizon,
                initial_state,
                sub_key,
                self.sampling_temperature,
            )
            rho = float(self._compiled_spec(traj.states, traj.times))
            all_traj.append(traj)
            all_rho.append(rho)
        chosen = int(jnp.argmax(jnp.asarray(all_rho)))
        diag: SamplerDiagnostics = {
            "sampler": "continuous_bon",
            "n_samples": self.n,
            "all_rho": all_rho,
            "chosen_index": chosen,
            "chosen_rho": all_rho[chosen],
            "max_rho": max(all_rho),
            "min_rho": min(all_rho),
            "final_rho": all_rho[chosen],
            "sampling_temperature": self.sampling_temperature,
        }
        return all_traj[chosen], diag


